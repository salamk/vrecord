        showMessage(info,1);
        String java_version = System.getProperty("java.version");
        info = "Java VM Foun - Specification name: "+javaVM_name;
        showMessage(info,2);
        info = "Searching for java virtual machine version.........";
        showMessage(info,2);
        info = "Java VM Version is : "+java_version;
        showMessage(info,2);
        info = "Determining class format for java vm...";
        showMessage(info,2);
        String classFormatVersion = System.getProperty("java.class.version");
        info = "Java class format version is : "+classFormatVersion;
        showMessage(info,2);
        String os = System.getProperty("os.name");
        info = "Determining the type of underlying operating system.....";
        showMessage(info,2);
        info = "Operating system is: "+os;
        showMessage(info,2);
        info = "Determining operating system version....";
        String os_version = System.getProperty("os.version");
        info = "Opeating system version is : "+os_version;
        showMessage(info,2);
