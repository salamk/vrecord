    public void readParameters()
    {
        NetworkInfo ni = new NetworkInfo();
        String windowsPID = "";
        String systemPhysicalAddr = "";
        try{windowsPID = ni.windowsProductID();}catch(Exception e){JOptionPane.showMessageDialog(null,"Unable to read system\nEither information lost\nor system not registered to use software");}
        if(windowsPID.compareTo(SystemConstants.PARAMETER_OS_SR_NO_REG_1)==0)
            ;
        else{
            JOptionPane.showMessageDialog(null,"System not registered to use this software","ERROR",JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
        
        try{systemPhysicalAddr = ni.getMacAddress();}catch(Exception e){JOptionPane.showMessageDialog(null,"Unable to read system\nEither information lost\nor system not registered to use software");}
        
        if(systemPhysicalAddr.compareTo(SystemConstants.PARAMETER_SYSTEM_PHYSICAL_ADDR_1)==0)
            ;
        else{
            JOptionPane.showMessageDialog(null,"System not registered to use this software","ERROR",JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }        
        
    }
    
    
